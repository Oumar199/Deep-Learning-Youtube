def update(gradients, parametres, learning_rate):

    C = len(parametres) // 2

    for c in range(1, C + 1):
        parametres["W" + str(c)] = (
            parametres["W" + str(c)] - learning_rate * gradients["dW" + str(c)]
        )
        parametres["b" + str(c)] = (
            parametres["b" + str(c)] - learning_rate * gradients["db" + str(c)]
        )
    return parametres
